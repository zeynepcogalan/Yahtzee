
public class Player {
	private Object name;
	 private Object score;
	 
	public Player(Object name, Object score) {
		super();
		this.name = name;
		this.score = score;
	}

	public Object getName() {
		return name;
	}

	public void setName(Object name) {
		this.name = name;
	}

	public Object getScore() {
		return score;
	}

	public void setScore(Object score) {
		this.score = score;
	}
	 
	 
	 
	 
}
